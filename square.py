# Функция для вычисления площади квадрата
# Принимает длину стороны квадрата a и возвращает его площадь.
# Пример использования:
# area(5)  # Возвращает 25
def area(a):
    return a * a


# Функция для вычисления периметра квадрата
# Принимает длину стороны квадрата a и возвращает его периметр.
# Пример использования:
# perimeter(5)  # Возвращает 20
def perimeter(a):
    return 4 * a

